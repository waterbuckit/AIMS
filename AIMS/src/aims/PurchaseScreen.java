    private boolean checkTotal(){
        boolean isZero = true;
        if(total == 0.00){
            isZero = false;
            return isZero;
        }else if (total > 0.01){
            isZero = true;
        }
        return isZero;
    }
    private void setTotal() {
        jLabel1.setText(String.format("%.2f", total));
    }

    private void addTheValue(double value) {
        previousTotals.add(total);
        total = total * 10;
        total += value;
        jButton10.setEnabled(checkTotal());
    }

    private void subtractFromToPay() {
        if (total < totalToPay) {
            totalToPay = totalToPay - total;
            AIMS.instance.purchaseList.jButton15.setText("To pay: £" + String.format("%.2f", totalToPay));
        }else if(total == totalToPay){
            purchaseComplete();
            AIMS.instance.purchaseList.jButton15.setText(null);
        } else {
            changeToGive = totalToPay - total;
            AIMS.instance.purchaseList.jButton15.setText("Change: £" + String.format("%.2f", changeToGive));
            purchaseComplete();
        }
    }
    private void purchaseComplete() {
        Receipt receipt = new Receipt(AIMS.instance.purchaseList.itemsToBuy, totalToPay, changeToGive, AIMS.instance.purchaseList.getUser(), AIMS.instance.status.getTransactionNum());
        receipt.makeReceipt();
        try {
            AIMS.instance.itemSelect.reset();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PurchaseScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        AIMS.instance.status.incrementTransaction();
        AIMS.instance.purchaseList.itemsToBuy.removeAll(AIMS.instance.purchaseList.itemsToBuy);
        AIMS.instance.purchaseList.listModel.removeAllElements();
        AIMS.instance.switchToScreen(AIMS.instance.itemSelect);
        AIMS.instance.frame.remove(this);
    }
